// 定数の宣言・値の代入
const shippingFee = 500;

// コンソールへの出力;
console.log(shippingFee);

const num = 50;
// もし定数numが10より大きいならば、「定数numは10より大きいです」という文字列を出力する
if (num > 10) {
  console.log("定数numは10より大きいです");
}

// 算術演算子を使った場合の戻り値を出力
console.log(45 + 18);

// 比較演算子を使った場合の戻り値を出力
console.log(45 > 18);

// 等価演算子を使った場合の戻り値を出力
console.log("5" == 5);

// 厳密等価演算子を使った場合の戻り値を出力
console.log("5" === 5);
